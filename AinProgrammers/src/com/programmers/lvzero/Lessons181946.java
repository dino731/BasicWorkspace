package com.programmers.lvzero;

import java.util.Scanner;

public class Lessons181946 {

	public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String a = sc.next();
        String b = sc.next();
        System.out.printf("%s%s",a,b);
        
        // %d : 10진수(decimal) 정수의 형식으로 출력
        // %f : 부동 소수점 숫자를 출력(소수점 이하 여섯 자리까지 출력)
        // %s : 문자열을 출력
        // %c : 문자 하나를 출력
        // %b : boolean 값을 출력 ('true'or'false')
        // %x : 16진수(hexadecimal) 정수를 출력
        // %e : 지수 표기법(exponential notation)을 사용하여 부동 소수점 숫자를 출력
        // %o : 8진수(octal) 정수를 출력
        // %n : 줄 바꿈 문자를 출력

	}

}
